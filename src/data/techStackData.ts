/* ─────────────────────────── COMPUTED ─────────────────────────── */
export function getStats() {
  const total = toolsData.length
  const favorites = toolsData.filter((t) => t.isFavorite).length
  const dailyUse = toolsData.filter((t) => t.badge === "Daily Use").length
  const categories = ALL_TOOL_CATEGORIES.length
  return { total, favorites, dailyUse, categories }
}

export function getAllTags(): { tag: string; count: number }[] {
  const map = new Map<string, number>()
  toolsData.forEach((t) => {
    t.tags.forEach((tag) => {
      map.set(tag, (map.get(tag) ?? 0) + 1)
    })
  })
  return Array.from(map.entries())
    .map(([tag, count]) => ({ tag, count }))
    .sort((a, b) => b.count - a.count)
}
